func init() {
	// 启动时检测系统语言
	currentLocale = detectSystemLocale()
}

// T 翻译菜单 key，线程安全
// 查找链: 当前语言 → 英语 → 返回 key 本身
func T(key string) string {
	localeMu.RLock()
	locale := currentLocale
	localeMu.RUnlock()

	if trans, ok := allTranslations[locale]; ok {
		if val, found := trans[key]; found {
			return val
		}
	}
	if trans, ok := allTranslations["en"]; ok {
		if val, found := trans[key]; found {
			return val
		}
	}
	return key
}

// SetLocale 运行时切换菜单语言
func SetLocale(locale string) {
	normalized := normalizeLocaleCode(locale)
	localeMu.Lock()
	currentLocale = normalized
	localeMu.Unlock()
}

// GetLocale 获取当前菜单语言
func GetLocale() string {
	localeMu.RLock()
	defer localeMu.RUnlock()
	return currentLocale
}

// detectSystemLocale 检测系统语言
func detectSystemLocale() string {
	var raw string

	switch runtime.GOOS {
	case "darwin":
		out, err := exec.Command("defaults", "read", "-g", "AppleLocale").Output()
		if err == nil {
			raw = strings.TrimSpace(string(out))
		}
	default:
		for _, env := range []string{"LANG", "LANGUAGE", "LC_ALL", "LC_MESSAGES"} {
			if val := os.Getenv(env); val != "" && val != "C" && val != "POSIX" {
				raw = strings.TrimSpace(val)
				break
			}
		}
	}

	return normalizeLocaleCode(raw)
}

// normalizeLocaleCode 将系统 locale 转换为内部 code
// "zh_CN.UTF-8" → "zh", "zh_TW" → "zh-TW", "en_US" → "en"
func normalizeLocaleCode(raw string) string {
	if raw == "" {
		return "en"
	}

	// 去掉编码后缀
	if idx := strings.IndexByte(raw, '.'); idx > 0 {
		raw = raw[:idx]
	}

	// 替换下划线
	raw = strings.ReplaceAll(raw, "_", "-")

	// 精确匹配
	lowered := strings.ToLower(raw)
	for code := range allTranslations {
		if strings.ToLower(code) == lowered {
			return code
		}
	}

	// 中文特殊处理
	prefix := strings.SplitN(raw, "-", 2)[0]
	if strings.ToLower(prefix) == "zh" {
		if strings.Contains(lowered, "tw") || strings.Contains(lowered, "hant") {
			return "zh-TW"
		}
		return "zh"
	}

	// 前缀匹配
	prefixLower := strings.ToLower(prefix)
	for code := range allTranslations {
		if strings.ToLower(strings.SplitN(code, "-", 2)[0]) == prefixLower {
			return code
		}
	}

	return "en"
}
